local opt = vim.opt

-- line number
opt.relativenumber = true
opt.number = true

-- indentation
opt.tabstop = 4
opt.shiftwidth = 4
opt.expandtab = true
opt.autoindent = true

-- wrap
opt.wrap = false

-- cursor
opt.cursorline = true

-- set default window right below
opt.splitright = true
opt.splitbelow = true

-- appearance
opt.termguicolors = true
opt.signcolumn = "yes"
